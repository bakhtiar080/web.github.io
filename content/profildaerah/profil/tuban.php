  <p>
Kabupaten Tuban adalah salah satu kabupaten di Jawa Timur yang terletak di Pantai Utara Jawa Timur. Kabupaten dengan jumlah penduduk sekitar 1,2 juta jiwa ini terdiri dari 20 kecamatan dan beribukota di Kecamatan Tuban. Kabupaten Tuban mempunyai letak yang strategis, yakni di perbatasan Provinsi Jawa Timur dan Jawa Tengah dengan dilintasi oleh Jalan Nasional Daendels di Pantai Utara. Pusat pemerintahan Kabupaten Tuban terletak 101 km sebelah barat laut Surabaya, ibu kota provinsi Jawa Timur dan 215 km sebelah timur Semarang, ibu kota provinsi Jawa Tengah. Oleh karena itu, pada zaman dahulu Tuban dijadikan pelabuhan utama Kerajaan Majapahit dan menjadi salah satu pusat penyebaran Agama Islam oleh para Walisongo.</p>
